package org.lfsmachinelearning.java.svm.main;

public class Pair {
    double x;
    double y;
    
    public Pair(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    public double getX() {
        return this.x;
    }
    
    public double getY() {
        return this.y;
    }
}
